# Função PRINT é usada para exibir coisas na tela, também chamada de ARGUMENTO (Argumento NÃO NOMEADO)

print(12, 34) # Os argumentos são separados pela vírgula
print(56, 78) # Por padrão ele quebra linha e aplica espaços
print(13, 46, sep='-') # Sep = SEPARADOR, pode separar os argumentos não nomeados 
print(13, 46, sep='-', end='##\n') # end = final do argumento  


# \r\n -> CRLF
# \n -< LF