# Tipo de dado bool (boolena)
# Ao questionar algo em um programa,
# Só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que,
# questiona se o valor é igual a outro.

print(10 == 10) # Questionando se o número 10 é igual a 10
# = Sim => True (Verdadeiro)

print(10 == 11) # Questionando se o número 10 é igual a 11
# = Não => False (Falso)

print(type(10 == 10)) # = bool (boolean)