# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letas minúsculas, pode usar: números e underline _
# O sinal de = é o operador de atribuição.
# Ele é usado para atribuir um valor a um nome (variável).
# Uso nome_variavel = expressão. (snake_case)

nome_completo = 'Marcus Lima'
print(nome_completo) # Marcus Lima

soma_dois_mais_dois = 2 + 2
print(soma_dois_mais_dois) # 4

int_um = int('1')
print(int_um, type(int_um)) # 1, int

nome = 'Marcus'
idade = 22
maior_de_idade = idade >= 18
print('Nome:', nome, 'Idade:', idade)
print('É de maior?', maior_de_idade)